tableau = [1, 3, 5, 7]

# ajouter l'élément 11 à la fin d'un tableau 
tableau.append(11)
print(tableau)

# retirer le dernier élément d'un tableau
# et le récupérer
élément = tableau.pop(-1)
print(tableau, élément)

# retirer le premier élément d'un tableau
# et le recupérer
élément = tableau.pop(0)
print(tableau, élément)

